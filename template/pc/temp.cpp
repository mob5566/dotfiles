#include <bits/stdc++.h>

using namespace std;

int main(int argc, char *argv[]) {
	int tt, ti;

	scanf("%d", &tt);

	for (ti = 1; ti <= tt; ++ti) {
		printf("Case #%d:", ti);

		fprintf(stderr, "## test case %d solved at %.3f sec ##\n",
			ti, (double)clock()/CLOCKS_PER_SEC);
	}

	return 0;
}
